package lesson4Kaz;

public class Calc {
    public static double getAddition(double x, double y) {
        return x + y;
    }

    public static double getSubtraction(double x, double y) {
        return x - y;
    }

    public static double getDivision(double x, double y) {
        return x / y;
    }

    public static double getMultiplication(double x, double y) {
        return x * y;
    }
}
